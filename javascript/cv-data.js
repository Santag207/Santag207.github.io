function getTechData(techName) {
    if (!techName) return null;

    // Try exact match
    if (cvData[techName]) return cvData[techName];

    // Try case-insensitive match
    const lowerName = techName.toLowerCase();
    const key = Object.keys(cvData).find(k => k.toLowerCase() === lowerName);
    if (key) return cvData[key];

    // Fallback data
    return {
        "description": `Experiencia profesional y académica utilizando ${techName}.`,
        "projects": ["Proyecto de ingeniería", "Implementación técnica"],
        "media": "img/iconos/generic-tech.png"
    };
}
